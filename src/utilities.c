#include "utilities.h"

/*******************************************************************************
*  
*  take char string and return calculated hash value
*  
*******************************************************************************/
uint32_t strhash(char* string)
{
	uint32_t hash = 17;
	int32_t index = 0;
	
	while (string && string[index]) {
		
		hash = (hash * 31) ^ string[index++];
	}
	
	return hash;
}

