#include <math.h>
#include "vector2d.h"

/*******************************************************************************
*  
*  function
*  
*******************************************************************************/
vec2d_t vector2d_normalise(vec2d_t vector_v)
{
	double length = sqrt(pow(vector_v.x, 2) + pow(vector_v.y, 2));
	vector_v.x /= length;
	vector_v.y /= length;
	return vector_v;
}

/*******************************************************************************
*  
*  function
*  
*******************************************************************************/
vec2d_t vector2d_add(vec2d_t vector_v, vec2d_t vector_w)
{
	vector_v.x += vector_w.x;
	vector_v.y += vector_w.y;
	return vector_v;
}

/*******************************************************************************
*  
*  function
*  
*******************************************************************************/
vec2d_t vector2d_sub(vec2d_t vector_v, vec2d_t vector_w)
{
	vector_v.x -= vector_w.x;
	vector_v.y -= vector_w.y;
	return vector_v;
}

/*******************************************************************************
*  
*  function
*  
*******************************************************************************/
double vector2d_dot(vec2d_t vector_v, vec2d_t vector_w)
{
	vector_v.x *= vector_w.x;
	vector_v.y *= vector_w.y;
	return vector_v.x + vector_v.y;
}

